package com.example.will.listviewquestionproject;

public class Personer {
    private String name;
    private int age;

    public Personer(String name, int i) {
        this.name = name;
        age = i;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
